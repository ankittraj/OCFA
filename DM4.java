public class DM4 {
    // This program demonstrates the use of if-else statements with block statements.
	public static void main(String[] args) {
		if(2<3){
			int b=5;
			System.out.println(b);
		}else {
			int b=6;
			System.out.println(b);
		}
		if(3<5) {
			int b=7;
			System.out.println(b);
		}
	}

}